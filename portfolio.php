			effectiveness of an optimization approach with two phases: generating
			an initial solution and then optimize it. 
		</p>
		<p class="pt">
			<strong>Tail Assignment</strong> é um problema de escolha dos melhores
			aviões para cada voo efetuado por uma companhia aérea. Estas companhias
			lidam com este problema de escalonamento por necessidade de cumprir os
			seus voos e minimizar os seus custos. Neste artigo analizamos a eficácia
			de uma otimização em duas fases: uma para geração de uma solução inicial
			e uma segunda para a sua otimização progressiva.
